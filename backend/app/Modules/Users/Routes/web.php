<?php

use Illuminate\Support\Facades\Route;
use App\Modules\Users\Controllers\UserController;

/*
|--------------------------------------------------------------------------
| USERS MODULE ROUTES
|--------------------------------------------------------------------------
| Prefix: admin/users
| Name: admin.users.*
|--------------------------------------------------------------------------
*/

Route::middleware(['admin'])
    ->prefix('admin/users')
    ->name('admin.users.')
    ->group(function () {

        /*
        |----------------------------------------------------------------------
        | LIST USERS
        |----------------------------------------------------------------------
        */
        Route::get(
            '/',
            [UserController::class, 'index']
        )->name('index');


        /*
        |----------------------------------------------------------------------
        | CREATE USER PAGE
        |----------------------------------------------------------------------
        */
        Route::get(
            '/create',
            [UserController::class, 'create']
        )->name('create');


        /*
        |----------------------------------------------------------------------
        | STORE USER
        |----------------------------------------------------------------------
        */
        Route::post(
            '/create',
            [UserController::class, 'store']
        )->name('store');


        /*
        |----------------------------------------------------------------------
        | EDIT USER PAGE
        |----------------------------------------------------------------------
        */
        Route::get(
            '/{id}/edit',
            [UserController::class, 'edit']
        )->name('edit');


        /*
        |----------------------------------------------------------------------
        | UPDATE USER
        |----------------------------------------------------------------------
        */
        Route::put(
            '/{id}',
            [UserController::class, 'update']
        )->name('update');


        /*
        |----------------------------------------------------------------------
        | DELETE USER
        |----------------------------------------------------------------------
        */
        Route::delete(
            '/{id}',
            [UserController::class, 'destroy']
        )->name('destroy');


        /*
        |----------------------------------------------------------------------
        | BLOCK / UNBLOCK USER
        |----------------------------------------------------------------------
        */
        Route::patch(
            '/{id}/toggle-status',
            [UserController::class, 'toggleStatus']
        )->name('toggle-status');


        /*
        |----------------------------------------------------------------------
        | RESTORE USER
        |----------------------------------------------------------------------
        */
        Route::patch(
            '/{id}/restore',
            [UserController::class, 'restore']
        )->name('restore');

    });